b = int(input('Segundo numero \n'))

while True:
    c = str(input('+ - * / \n'))
    if c == '+':
        print(a + b)
        break
    if c == '-':
        print(a - b)
        break
    if c == '*':
        print(a * b)
        break
    if c == '/':
        print(a / b)
        break
